int		ft_sqrt(int nb)
{
	int tmp;

	tmp = 0;
	if (nb < 0)
		return (0);
	while ((tmp * tmp) <= nb)
	{
		if ((tmp * tmp) < 0)
			return (0);
		if (tmp * tmp == nb)
			return (tmp);
		tmp++;
	}
	return (0);
}
